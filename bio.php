            <div class="bio-peter">
              <p class="bio-peter"><h3>I’m truly grateful to you taking the time to browse through my work.
                How did it all begin? Well I remember as a child being excited by cartoons
                 and comics.  I watched animation religiously always being fascinated at the
                  notion that they were drawings moving before my very eyes and I collected 
                  numerous amounts of comics particularly Marvel and DC.  In terms of cartoons
                   I was very struck by the old school hand drawn animation by the likes
                    of Warner Bros, Disney,  Hanna Barbera to name but a few.
                      I remember making flipbook animation as well as drawing
                        from comic books and then I kinda knew what I wanted 
                        to do after I left school.  I had big dreams of becoming
                         an animator and producing my own animated films, which
                          I managed to do with the aid of my Super 8 cine camera.</h3></p>
            
              <strong>1987/88:</strong><p class="bio-peter">
              After leaving college having studied for a year Graphic Design.
                I got my first job ever in the animation business as a Trainee
                 Animator working for Brian Stevens Animated Films, which was
                  situated off  Oxford St.  Feeling really elated at the idea
                   that I would finally get my break, being the youngest and
                    the last one in.  I had to make the tea and fetch and carry
                     stuff to and from the studio.  There were periods of time when
                      there was nothing to do, during which time I would either read
                       a book about animation or doodle. I  do remember one of the
                        first projects that I was involved with which was a sex
                         education video aimed at teenagers called ‘No Worries’,
                          where I would paint mattes that would be used as part of
                           creating the visual effects on the production, as well
                            as painting sperm yes you read correctly sperm. 
                             In addition to this I would render typography
                              for other projects, mix up colours and paint acetates
                               or what was known in the industry as ‘cells’, which
                                were transparent sheets of plastic on which drawings
                                 would be traced and painted on to.  Another project 
                                 I recall working on was for a company named ‘Kvernaland’ 
                                  which was a film that demonstrated how a plough that they 
                                  had built operated</p>
              <strong>1988/89</strong>
                <p class="bio-peter">After leaving Brian Stevens Animated Films.
                  I came across a studio named Richard Taylor Cartoon Films based
                   in Muswell Hill north London and they were producing an animated 
                   production on behalf of the BBC known as ‘English by Television’ 
                   also known as ‘Muzzy Comes Back’ a sequel to a previous production
                    that they made  called ‘Muzzy in Gondoland’.
                      The aim of this production was to teach children
                       about reading the English language and how to tell the time,
                        the central character was Muzzy, who was a big green friendly
                         furry alien who had a strange appetite for clocks.
                           My role in this production was mainly to operate
                            the line testing camera, which was used to film the drawings
                             at the pencil stage to check the animation before it went onto
                              being traced and painted on to cells.  In addition to that 
                              collecting and delivering, making the tea, painting and tracing
                               on to cells, worked on some of the visual effects shots, and I
                                finally got my first crack at drawing the inbetweens for some
                                 of the animation. How cool is that? I thought.</p>
                <strong>1990/91</strong>
                  <p class="bio-peter">After leaving  Richard Taylor Cartoon Films.
                    I received a phone call from Hollywood Film Productions to work 
                    on a feature length animated film called ‘Freddie as FR07’, 
                    which was loosely based on James Bond, where the hero being
                     Freddie is a secret agent frog who is brought in to solve 
                     the mystery of various monuments disappearing from around the world.
                       Working on this production was by default as I really wanted 
                       to work on ‘Fievel Goes West’ which was produced by
                        Steven Spielberg being made in London at the time, 
                        but sadly they wouldn’t have me boo hoo, ahh well back to Freddie. 
                         My role on Freddie was as assistant animator and clean-up artist, 
                         where I would draw the in between drawings and neaten the animator’s
                          rough drawings. Despite the characters being voiced by a distinguished
                           cast of fine actors such as: Brian Blessed, Jonathan Pryce,
                            Jenny Agutter and of course Ben Kingsley who was the voice of
                             Freddie, it didn’t do as well as hoped at the box office.</p>
                  <strong>1992 onwards</strong>
                    <p class="bio-peter">I have worked on a number of other projects since leaving
                     Hollywood Road Film Productions which includes still cartoons
                      for some of the shows for an online alternative news and
                       current affairs programme  for an organisation called RADTV UK,
                         archives available on You Tube.  My work nowadays is centred
                          around mainly looking for opportunities in which I can widen
                           the appeal of cartooning into cartoon drawing workshops for
                            individuals or organisations, businesses, corporations, caricaturing
                             at events.  
                             I can and have designed my own characters,
                              produced my own animated films on Super 8
                               and I have created my own artwork available for sale.
                                 In addition to that I am open to collaborations with
                                  other artists, writers and create images for branding
                                   purposes ,presentations and open to commissions to create
                                    artwork as personal gifts.</p>
